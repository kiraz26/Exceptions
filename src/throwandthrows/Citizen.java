package throwandthrows;

public class Citizen {
	
	private String name;
	private int age;
	//getters and setters
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	//constructor
	public Citizen(String name, int age) {
		super();
		this.name = name;
		this.age = age;
	}
	
	
	
	
	
}
